import 'package:flutter/material.dart';

/// Create reuseable padding.

/// [defaultPadding]
///
/// Padding all side by 16.
const EdgeInsets defaultPadding = EdgeInsets.symmetric(
  horizontal: 16,
  vertical: 16,
);

/// [halfPadding]
///
/// Padding all side by 9.
const EdgeInsets halfPadding = EdgeInsets.symmetric(
  horizontal: 8,
  vertical: 8,
);

/// [smallPadding]
///
/// Padding all side by 9.
const EdgeInsets smallPadding = EdgeInsets.symmetric(
  horizontal: 4,
  vertical: 4,
);

/// [horizontalPadding]
///
/// Padding horizontally at left and right by 16.
const EdgeInsets horizontalPadding = EdgeInsets.symmetric(horizontal: 16);

/// [halfHorizontalPadding]
///
/// Padding horizontally at left and right by 8.
const EdgeInsets halfHorizontalPadding = EdgeInsets.symmetric(horizontal: 8);

/// [smallHorizontalPadding]
///
/// Padding horizontally at left and right by 4.
const EdgeInsets smallHorizontalPadding = EdgeInsets.symmetric(horizontal: 4);

/// [verticalPadding]
///
/// Padding vertically at top and bottom by 16.
const EdgeInsets verticalPadding = EdgeInsets.symmetric(vertical: 16);

/// [halfVerticalPadding]
///
/// Padding vertically at top and bottom by 8.
const EdgeInsets halfVerticalPadding = EdgeInsets.symmetric(vertical: 8);

/// [smallVerticalPadding]
///
/// Padding vertically at top and bottom by 4.
const EdgeInsets smallVerticalPadding = EdgeInsets.symmetric(vertical: 4);

/// [zeroPadding]
///
/// Padding all side by 0.
const EdgeInsets zeroPadding = EdgeInsets.zero;
